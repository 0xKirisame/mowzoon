// Arabic nudges mirror engine.py's branches (the API only returns English text)
export function nudgeAr(id, metrics, spike, fmtNum, fmtDays) {
  const eff = Math.round(metrics.efficiency);
  const res = Math.round(metrics.resilience);
  const eq = Math.round(metrics.eq);
  if (id === 0) {
    return eq < 40
      ? `لاحظنا أن الإنفاق في وقت متأخر من الليل يُثقل ذكاءك المالي العاطفي (${fmtNum(eq)}/${fmtNum(100)}). خذ لحظة هدوء الليلة.`
      : 'أحسنت في ضبط المشتريات الاندفاعية مؤخرًا. واصل على هذا المنوال.';
  }
  if (id === 1) {
    return eff < 50
      ? `مدخراتك رائعة، لكن كفاءة إنفاقك ${fmtNum(eff)}/${fmtNum(100)}. لا بأس أن تدلّل نفسك بقهوة اليوم، دون ذنب.`
      : 'توازنك بين الادخار والاستمتاع بالحياة مثالي. حافظ عليه.';
  }
  if (id === 2) {
    return res < 40
      ? `مرونتك الاستباقية منخفضة جدًا (${fmtNum(res)}/${fmtNum(100)}). فكّر في إبقاء سيولة أكبر بدل الإفراط في الاستثمار هذا الأسبوع.`
      : 'احتياطي السيولة لديك يتعافى. عمل رائع في حماية استثماراتك.';
  }
  return spike
    ? `«${SPIKE_AR[spike.name] ?? spike.name}» ${fmtDays(spike.days)}. مبلغ صغير تضعه جانبًا الآن خير من ارتباك لاحق.`
    : 'لا ذروات في الأفق حاليًا. الهدوء هو لحظة الادخار الصغيرة.';
}

